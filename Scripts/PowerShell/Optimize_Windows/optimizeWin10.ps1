### HELPER FUNCTIONS
function Remove-RegistryItem {
    param(
        [Parameter(Mandatory = $true)]
        [string]$Path
    )

    try {
        Remove-Item -Path $Path -ErrorAction Stop
        Write-Host "Item removed: $Path"
    }
    catch {
        if ($_.Exception.Message -like "*cannot find path*") {
            Write-Host "Already removed: $Path"
        }
        else {
            throw $_
        }
    }
}

